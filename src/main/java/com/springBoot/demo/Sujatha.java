package com.springBoot.demo;

public class Sujatha {
	private String Name;
	private int id;
	
	public Sujatha(String name, int id) {
		super();
		Name = name;
		this.id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	

}
